#[derive(Clone, Debug, PartialEq)]
pub enum TokenKind {
    Var,
    Def,
    Return,
    If,
    Elif,
    Else,
    Loop,
    While,
    Async,
    Await,
    Break,
    Continue,
    True,
    False,
    Ident(String),
    Number(i64),
    Str(String),
    LParen,
    RParen,
    LBrace,
    RBrace,
    Comma,
    Semicolon,
    Dot,
    Plus,
    Minus,
    Star,
    Slash,
    Eq,
    EqEq,
    Ne,
    Gt,
    Lt,
    Ge,
    Le,
    AndAnd,
    OrOr,
    Bang,
    Eof,
}

#[derive(Clone, Debug)]
pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
    pub col: usize,
}